void print_addr(unsigned int addr, int bytelength, int bin_or_hex)
{
	int i, k;
	u32 *addrPtr;
	addrPtr = (u32 *)addr;
	Xil_DCacheInvalidateRange(addrPtr, bytelength);

	for (k = 0; k < (bytelength/4); k++) {
		if (bin_or_hex)
			xil_printf("%08X", addrPtr[k]);
		else
			for (i=0; i<32; i++) {
				if (addrPtr[k] & 0x80000000)
					xil_printf("1");
				else
					xil_printf("0");
				addrPtr[k] <<= 1;
			}
		xil_printf("\r\n");
	}
}

