/*****************************************************************************/
/*
*
* This function checks data buffer after the DMA transfer is finished.
*
* @param	None
*
* @return
*		- XST_SUCCESS if validation is successful.
*		- XST_FAILURE otherwise.
*
* @note		None.
*
******************************************************************************/
static int CheckDataNow(void)
{
	u32 *RxPacket;
	u32 Index = 0;

	RxPacket = (u32 *) RX_BUFFER_BASE;

	//RxPacket[MAX_PKT_LEN_WORDS-1] = MAX_PKT_LEN_WORDS-1;
	/* Invalidate the DestBuffer before receiving the data, in case the
	 * Data Cache is enabled
	 */
	Xil_DCacheInvalidateRange((u32)(RxPacket+0), MAX_PKT_LEN);
	for(Index = 0; Index < MAX_PKT_LEN_WORDS; Index++) {
		if((Index >= 0) && (Index < 5))
			xil_printf("%d ", (unsigned int)RxPacket[Index]);
		if((Index >= MAX_PKT_LEN_WORDS-5) && (Index < MAX_PKT_LEN_WORDS))
			xil_printf("%d ", (unsigned int)RxPacket[Index]);
	}
	xil_printf("\r\n");

	Xil_DCacheInvalidateRange((u32)(RxPacket+OFFSET), MAX_PKT_LEN);
	for(Index = OFFSET; Index < MAX_PKT_LEN_WORDS+OFFSET; Index++) {
		if((Index >= OFFSET) && (Index < (OFFSET+5)))
			xil_printf("%d ", (unsigned int)RxPacket[Index]);
		if((Index >= MAX_PKT_LEN_WORDS+OFFSET-5) && (Index < MAX_PKT_LEN_WORDS+OFFSET))
			xil_printf("%d ", (unsigned int)RxPacket[Index]);
	}
	xil_printf("\r\n");

	return XST_SUCCESS;
}


static int CheckData(int lengthbyte, u32 AddrBase)
{
	u32 *Addr;
	u32 Index = 0;

	Addr = (u32 *) AddrBase;

	Xil_DCacheInvalidateRange((u32)Addr, lengthbyte);
	for(Index = 0; Index < lengthbyte/4; Index++) {
		xil_printf("%08X\r\n", (unsigned int)Addr[Index]);
	}
	xil_printf("\r\n");

	return XST_SUCCESS;
}

static int copy_temp()
{
	u32 *UxBufferPtr;
	u32 *CxBufferPtr;
	u32 *DxBufferPtr;
	u32 *TempBufferPtr;

	u32 length;
	u32 j;
	u32 offset_C_data;

	CxBufferPtr = (u32 *)C_BUFFER_BASE;
	DxBufferPtr = (u32 *)D_BUFFER_BASE;
	TempBufferPtr = (u32 *)TEMP_BUFFER_BASE;

	CxBufferPtr[0] = 0x00004000;
	CxBufferPtr[1] = 0x00000400;
	CxBufferPtr[2] = 0x00000010;
	offset_C_data = 2047;
	for(j=0; j<32; j++) {
		CxBufferPtr[j+3] = TempBufferPtr[j+offset_C_data];
	}
	offset_C_data = 35;
	length = (2047+offset_C_data)*4; // compressed data without header
	for(j=0; j<length; j++) {
		CxBufferPtr[j+offset_C_data] = TempBufferPtr[j];
	}
	Xil_DCacheFlushRange((u32)CxBufferPtr, 8328);
}

static int clear_mem()
{
	u32 *UxBufferPtr;
	u32 *CxBufferPtr;
	u32 *DxBufferPtr;
	u32 *TempBufferPtr;
	u32 j;

	UxBufferPtr = (u32 *)U_BUFFER_BASE;
	CxBufferPtr = (u32 *)C_BUFFER_BASE;
	DxBufferPtr = (u32 *)D_BUFFER_BASE;
	TempBufferPtr = (u32 *)TEMP_BUFFER_BASE;

	for (j = 0L; j < MAX_PKT_LEN_WORDS*16; j++) {
		CxBufferPtr[j] = 0;
	}
	Xil_DCacheFlushRange((u32)CxBufferPtr, MAX_PKT_LEN*16);

	for (j = 0L; j < MAX_PKT_LEN_WORDS*16; j++) {
		DxBufferPtr[j] = 0;
	}
	Xil_DCacheFlushRange((u32)DxBufferPtr, MAX_PKT_LEN*16);

	for (j = 0L; j < MAX_PKT_LEN_WORDS*16; j++) {
		TempBufferPtr[j] = 0;
	}
	Xil_DCacheFlushRange((u32)TempBufferPtr, MAX_PKT_LEN*16);
}

static int init_dma_d()
{
		XAxiDma_Config *CfgPtr;
		int Status;

	CfgPtr = XAxiDma_LookupConfig(DMA_DEV_ID_SDRAM2FIFO_D);
	if (!CfgPtr) {
		xil_printf("No config found for %d\r\n", DMA_DEV_ID_SDRAM2FIFO_D);
		return XST_FAILURE;
	}

	Status = XAxiDma_CfgInitialize(&AxiDma_SDRAM2FIFO_D, CfgPtr);
	if (Status != XST_SUCCESS) {
		xil_printf("Initialization failed %d\r\n", Status);
		return XST_FAILURE;
	}

	if(XAxiDma_HasSg(&AxiDma_SDRAM2FIFO_D)){
		xil_printf("Device configured as SG mode \r\n");
		return XST_FAILURE;
	}

	CfgPtr = XAxiDma_LookupConfig(DMA_DEV_ID_FIFO2SDRAM_D);
	if (!CfgPtr) {
		xil_printf("No config found for %d\r\n", DMA_DEV_ID_FIFO2SDRAM_D);
		return XST_FAILURE;
	}

	Status = XAxiDma_CfgInitialize(&AxiDma_FIFO2SDRAM_D, CfgPtr);
	if (Status != XST_SUCCESS) {
		xil_printf("Initialization failed %d\r\n", Status);
		return XST_FAILURE;
	}

	if(XAxiDma_HasSg(&AxiDma_FIFO2SDRAM_D)){
		xil_printf("Device configured as SG mode \r\n");
		return XST_FAILURE;
	}

	/* Disable interrupts, we use polling mode
	 */
	XAxiDma_IntrDisable(&AxiDma_SDRAM2FIFO_D, XAXIDMA_IRQ_ALL_MASK,
						XAXIDMA_DEVICE_TO_DMA);
	XAxiDma_IntrDisable(&AxiDma_SDRAM2FIFO_D, XAXIDMA_IRQ_ALL_MASK,
						XAXIDMA_DMA_TO_DEVICE);

	XAxiDma_IntrDisable(&AxiDma_FIFO2SDRAM_D, XAXIDMA_IRQ_ALL_MASK,
						XAXIDMA_DEVICE_TO_DMA);
	XAxiDma_IntrDisable(&AxiDma_FIFO2SDRAM_D, XAXIDMA_IRQ_ALL_MASK,
						XAXIDMA_DMA_TO_DEVICE);
}

static int init_u_data()
{
	u32 Index;
	u32 *UxBufferPtr;
	u32 Value;

	UxBufferPtr = (u32 *)U_BUFFER_BASE;

	Value = 0;
	for(Index = 0; Index < MAX_PKT_LEN_WORDS*16; Index++) {
			UxBufferPtr[Index] = 0x00000000 + Value;
			Value++;
	}
}

